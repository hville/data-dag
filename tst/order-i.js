var t = require('cotest'),
		s = require('../src/order-i')

t('sort - valid order', function() {
	t('{==}', s(['c','b','a','d'], [{s:2},{s:1},{s:0},{s:3}]), ['a','b','c','d'])
	t('{==}', s(['c','a','d','b'], [{s:1},{s:3},{s:0},{s:2}]), ['a','b','c','d'])
	t('{==}', s(['c','d','a','b'], [{s:2},{s:3},{s:0},{s:1}]), ['a','b','c','d'])
	t('{==}', s(['d','a','b','c'], [{s:1},{s:2},{s:3},{s:0}]), ['a','b','c','d'])
	t('{==}', s(['b','a','c','e','d'], [{s:1},{s:0},{s:2},{s:4},{s:3}]), ['a','b','c','d','e'])
	t('{==}', s(['b','e','c','a','d'], [{s:3},{s:0},{s:2},{s:4},{s:1}]), ['a','b','c','d','e'])
})
