var t = require('cotest'),
		s = require('../src/order-i')

t('sort - valid order', function() {
	t('{==}', s.call([{i:2},{i:1},{i:0},{i:3}], ['c','b','a','d']), ['a','b','c','d'])
	t('{==}', s.call([{i:1},{i:3},{i:0},{i:2}], ['c','a','d','b']), ['a','b','c','d'])
	t('{==}', s.call([{i:2},{i:3},{i:0},{i:1}], ['c','d','a','b']), ['a','b','c','d'])
	t('{==}', s.call([{i:1},{i:2},{i:3},{i:0}], ['d','a','b','c']), ['a','b','c','d'])
	t('{==}', s.call([{i:1},{i:0},{i:2},{i:4},{i:3}], ['b','a','c','e','d']), ['a','b','c','d','e'])
	t('{==}', s.call([{i:3},{i:0},{i:2},{i:4},{i:1}], ['b','e','c','a','d']), ['a','b','c','d','e'])
})
